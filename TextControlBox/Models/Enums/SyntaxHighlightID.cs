namespace TextControlBoxNS;

/// <summary>
/// Represents the unique identifiers for different code languages used for syntax highlighting.
/// </summary>
public enum SyntaxHighlightID
{
    /// <summary>
    /// Identifier for x86 assembly.
    /// </summary>
    x86Assembly,

    /// <summary>
    /// Identifier for Batch code language.
    /// </summary>
    Batch,

    /// <summary>
    /// Identifier for C++ code language.
    /// </summary>
    Cpp,

    /// <summary>
    /// Identifier for C# code language.
    /// </summary>
    CSharp,

    /// <summary>
    /// Identifier for klipper like config styles.
    /// Klipper is a 3d printer firmware, which can be configured via printer.cfg files
    /// </summary>
    Klipper,

    /// <summary>
    /// Identifier for ini configuration style.
    /// </summary>
    Inifile,

    /// <summary>
    /// Identifier for CSS code language.
    /// </summary>
    CSS,

    /// <summary>
    /// Identifier for CSV code language.
    /// </summary>
    CSV,

    /// <summary>
    /// Identifier for GCode code language.
    /// </summary>
    GCode,

    /// <summary>
    /// Identifier for HexFile code language.
    /// </summary>
    HexFile,

    /// <summary>
    /// Identifier for HTML code language.
    /// </summary>
    Html,

    /// <summary>
    /// Identifier for Java code language.
    /// </summary>
    Java,

    /// <summary>
    /// Identifier for JavaScript code language.
    /// </summary>
    Javascript,

    /// <summary>
    /// Identifier for JSON code language.
    /// </summary>
    Json,

    /// <summary>
    /// Identifier for LaTeX code language.
    /// </summary>
    Latex,

    /// <summary>
    /// Identifier for Lua programminglanguage.
    /// </summary>
    Lua,
    /// <summary>
    /// Identifier for Markdown code language.
    /// </summary>
    Markdown,

    /// <summary>
    /// Identifier for PHP code language.
    /// </summary>
    PHP,

    /// <summary>
    /// Identifier for Python code language.
    /// </summary>
    Python,

    /// <summary>
    /// Identifier for Q# code language.
    /// </summary>
    QSharp,

    /// <summary>
    /// Identifier for SQL language.
    /// </summary>
    SQL,

    /// <summary>
    /// Identifier for TOML style config files.
    /// </summary>
    TOML,

    /// <summary>
    /// Identifier for XML code language.
    /// </summary>
    XML,

    /// <summary>
    /// Identifier for no code language.
    /// </summary>
    None,
}
